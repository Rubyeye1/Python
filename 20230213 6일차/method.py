name = "nico"
print(name.upper())         # 대문자 변환
print(name.capitalize())    # 첫글자만 대문자
print(name.startswith("n")) # n으로 시작하는지 검사
print(name.replace("o","ㅁ")) # 바꾸기
print(name.endswith("o"))   # o로 끝나는지 검사
print("nico".endswith("o"))   # o로 끝나는지 검사

